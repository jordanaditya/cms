          <p>Sebuah PC workstation biasanya menghadirkan spesifikasi dengan kelas teratas, dimana semua komponen yang digunakan adalah kelas khusus workstation, yang harus dapat bertahan dalam waktu pemakaian kerja secara terus menerus. Lalu bagaimana jika spesifikasi tersebut dihadirkan dalam bentuk laptop. Seperti laptop yang hadir di meja pengujian Jagat Review berikut ini, yaitu Lenovo ThinkPad P15s.</p>

          <p>Lenovo ThinkPad P15s adalah Mobile Workstation kelas Entry Level dari Lenovo. Spesifikasi yang ditawarkan disesuaikan dengan kebutuhan pengguna untuk kebutuhan kerja profesional. Laptop ini mengantongi beberapa standar tersendiri yang jarang kita temukan pada perangkat laptop mainstream. Baik dari segi hardware maupun software. Terutama soal kompatibilitasnya pada software-software kerja seperti desain, render 3D dan lain-lain.
             Seperti apa performa sesungguhnya dari laptop yang satu ini, simak review lengkap Lenovo ThinkPad P15s berikut ini.</p>

          <h2 class="section-heading">Spesifikasi</h2>

          <p>Untuk spesifikasi dari Lenovo ThinkPad P15s sendiri bisa sangat beragam, karena perangkat tersedia melalui jalur Project sehingga spesifikasi dan fitur dapat disesuaikan dengan kebutuhan perusahaan. Jadi spesifikasi dan fitur yang didapatkan mungkin akan sama atau berbeda sama sekali dengan unit yang kami uji kali ini.</p>

          <p>Adapun perangkat Lenovo ThinkPad P15s yang kami review ini memiliki spesifikasi diantaranya yaitu Processor Intel Core i7 10610U, yang merupakan Intel Core i7 10510U dengan dukungan teknologi Intel vPro.</p>

          <p>Laptop yang kami uji ini juga menggunakan Discrete Graphics yaitu NVIDIA Quadro GPU, yang didukung oleh arsitektur Pascal. NVIDIA Quadro P520 memiliki shared memory 2 GB GDDR5 dan 384 CUDA Cores.
             Lenovo membekali produk ini dengan GPU Nvidia Quadro P520, karena produk PC kelas Workstation dari Lenovo selalu menggunakan NVIDIA Quadro. NVIDIA Quadro P520 telah mengantongi ISV (Independent Software Vendor) Certification dari sejumlah Developer ternama seperti 3DEXPERIENCE, Adobe, ANSYS, ArcGIS, AutoCAD, Creo, MicroStation, NX, Revit, Solid Edge dan SOLIDWORKS.
             Quadro dikembangkan oleh Nvidia dengan optimalisasi untuk aplikasi CAD (Computer Aided Design) atau aplikasi kelas profesional lainnya. Sehingga untuk kebutuhan kerja, akan menampilkan performa dan stabilitas lebih baik.
             Selain itu NVIDIA Quadro mendukung 32-bit Color baik di mode DirectX maupun OpenGL. Dukungan lainnya dari Nvidia yaitu Support langsung ke pengguna Quadro, termasuk permintaan perbaikan secara spesifik.</p>

          <h2 class="section-heading">Harga</h2>

          <p>Harga per-unit Lenovo ThinkPad P15s memang akan dipengaruhi oleh spesifikasi Hardware dan fitur yang dibutuhkan perusahaan. Untuk spesifikasi Hardware dan fitur dasar, harga Lenovo ThinkPad P15s dimulai dari  Rp.30 juta rupiah.</p>

